class Switchdemo{

	public static void main(String[] args){

		String str="Mon";

		switch(str){

			case "Mon":
				System.out.println("Monday");
				break;

			case "Tue":
				System.out.println("Tuseday");
                                break;

			default:
				System.out.println("Sunday");
				break;
		}
	}
}



